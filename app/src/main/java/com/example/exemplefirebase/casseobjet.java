package com.example.exemplefirebase;

public class casseobjet {
    String un;
    String deux;
    String trois;

    public casseobjet(String un, String deux, String trois) {
        this.un = un;
        this.deux = deux;
        this.trois = trois;
    }

    public String getUn() {
        return un;
    }

    public void setUn(String un) {
        this.un = un;
    }

    public String getDeux() {
        return deux;
    }

    public void setDeux(String deux) {
        this.deux = deux;
    }

    public String getTrois() {
        return trois;
    }

    public void setTrois(String trois) {
        this.trois = trois;
    }
}
